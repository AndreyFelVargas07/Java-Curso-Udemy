
public class ConcatenacionVariables {

    public static void main(String[] args) {

        //Concatenando cadenas 
        var nombre = "Andrey";
        var titulo = " Ing.Software";
        var union = nombre + titulo;

        System.out.println("Hola " + "Sr." + union);
        
        //Concatenando Enteros
        var num1 = 8;
        var num2 = 18;
        System.out.println("Suma = " + (num1 + num2)); //Suma de numeros

    }
}
